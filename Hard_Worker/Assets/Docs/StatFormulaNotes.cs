// 실제 코드로 사용되지 않으며, 참고용

// ------------------------------
// Cut (식재료 손질)
// ------------------------------
// - 기본 수치: 10, 100 등 정수
// - 업그레이드 시: +10, +20 등 정수 단위로 증가
// - 유저가 가장 많이 레벨업하게 되는 핵심 능력치
// - 최종 데미지 = Cut + 장비 수치
// - 재료의 완성도 게이지를 올려 처치하는 데미지로 작용

// ------------------------------
// CritChance (완벽한 손질 확률)
// ------------------------------
// - 기본 수치: 1.0
// - 업그레이드 시: +0.5, +1 등 미세하게 증가
// - 퍼센트 확률로 계산 (예: 10 → 10%)
// - 클릭 시 일정 확률로 CritBonus 발동

// ------------------------------
// CritBonus (완벽한 손질 보너스)
// ------------------------------
// - 기본 수치: 100
// - 업그레이드 시: +5, +10 등 (CritChance보다 큰 폭 증가)
// - 완벽 손질 성공 시 추가 데미지 보너스 적용

// [계산식]
// 최종 데미지 = 기본 데미지 × (1 + CritBonus / 100)
//
// 예시:
// - CritBonus = 0   → ×1.0배 (보너스 없음)
// - CritBonus = 100 → ×2.0배 (기본 데미지 2배)
// - CritBonus = 150 → ×2.5배

// ------------------------------
// Income (수익 증가)
// ------------------------------
// - 기본 수치: 0
// - 업그레이드 시: +5, +10
// - 최종 수익 = 기본 수익 × (1 + Income%)
// - 예: Income = 10 → 100 × (1 + 0.10) = 110 지급

// ------------------------------
// AssistSpeed (보조 셰프 속도)
// ------------------------------
// - 0레벨 시 보조 셰프 비활성화 (코드 상 if 처리)
// - 업그레이드 시 수치가 감소: 5 → 4.8 → 4.6...
// - 수치 = 공격 쿨타임(초)
// - 예: 3.0이면 3초마다 자동 공격 1회

// ------------------------------
// AssistSkill (보조 셰프 숙련도)
// ------------------------------
// - 기본 수치: 10, 100 등 정수
// - 업그레이드 시: +10, +20
// - 보조 셰프 공격 시 데미지로 적용됨
// - 손질(Cut)과 유사하나, 성장 속도는 느리고 비용은 큼
